package learned;

public class Division {
    public static void main(String[] args) {
        int dividend = 25;
        int divisor = 4;

        double quotient = dividend/divisor;
        int remainder = dividend%divisor;

        System.out.println("Quotient is: "+quotient);
        System.out.println("Quotient is: "+remainder);

    }
}
